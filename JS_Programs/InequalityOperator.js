
// The inequality operator != performs the opposite function of the equality operator.
// It accepts 2 inputs of any type and outputs false if they are equal and true if the
// are not equal.

console.log(1!=1);   // false
console.log(1!=2);    // true
console.log("Masai"!="masai");  // true
console.log("Masai"!=="masai");  // true
console.log("Masai"!="Masai");  // false
console.log("Masai"!=="Masai");  //false

// Similar to === , !== will check for type as well.
// It is recommended to use === and !== when it comes to comparison operators